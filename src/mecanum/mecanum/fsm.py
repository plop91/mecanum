# States

# State: 0 - Idle: The robot is not moving, operating in manual mode
# State: 1 - Stop: The robot is potentially moving, but the user has requested a stop
# State: 2 - Manual: The robot is moving, and the user has requested manual control
# State: 3 - Automatic: The robot is potentially moving, and the user has relinquished control
# State: 4 - Error: The robot has encountered an error, and is stopping